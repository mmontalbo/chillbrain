
function infoBox() {
	var el = $("<div />");				
	el.attr("title","instructions");
	el.attr("id","instructions");
	el.prependTo("body");
	
	changeInstructionsText("Of the two images, select the one you like better.<br/><br/><br/> Press 'spacebar' to skip.");
}

function changeInstructionsText(htmlString) {
	$("div#instructions").html(htmlString);
}
